public class canFly {
    public static void main(String[] args){
        canFly(true);
        canFly(false);
    }
    public static void canFly(boolean isBird){
        if(isBird==true){
            System.out.println("Can fly");
        }
        else{
            System.out.println("Cant fly");
        }
    }
}
